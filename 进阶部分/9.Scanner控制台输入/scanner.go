package main

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	// ====================== 学习警示注释（复制到你项目顶部的提醒） ======================
	/*
		【输入函数避坑总结】
		1. fmt.Scanf / fmt.Scan / fmt.Scanln 不要混用！
		   原理：用户回车产生的换行符 \n 会残留在输入缓冲区
		   - Scanf：只读取匹配格式数据，不清理换行
		   - Scanln：读取一整行，缓冲区有残留换行会直接读到空，跳过输入
		   故障：菜单选数字后，下一次输入直接跳过、疯狂提示输入错误（聊天室bug根源）
		2. bufio.Scanner 优势（交互式程序首选）
		   ① 一次性读取完整一行，自动丢弃末尾 \n，无缓冲区残留
		   ② 原生支持带空格、中文、长文本（昵称/聊天消息）
		   ③ 统一一套输入逻辑，不用混用多种输入函数
		   ④ 先拿到原始字符串，再手动转换数字，自定义校验更灵活
		3. 使用区分
		   ✅ Scanner：菜单交互、聊天输入、多轮连续输入、带空格内容
		   ⚠️ Scanf仅适合：一次性单数字读取，读完程序直接退出，无后续输入
	*/

	// ==============================================================================
	// 模块1：读取单行字符串（输入姓名）
	// 老式写法：fmt.Scanln（有缓存残留坑）
	// ==============================================================================
	fmt.Println("========= 模块1：读取单行字符串 =========")
	fmt.Print("请输入你的姓名：")
	/*
		// 老式方案 注释掉 存在缓冲区遗留换行问题
		var nameOld string
		fmt.Scanln(&nameOld)
		fmt.Println("老式Scanln读取：", nameOld)
	*/

	// Scanner 标准写法
	scanner := bufio.NewScanner(os.Stdin)
	//bufio.NewScanner()：创建扫描器实例scanner，专门用于流式文本读取。
	//os.Stdin：绑定标准控制台输入，也就是我们键盘输入的数据流。
	//等价翻译：创建一个专门监听键盘输入的读取器，后续所有输入都由它接管。

	if scanner.Scan() {
		//scanner.Scan()
		//阻塞特性：代码运行到这里会暂停卡住，一直等待你键盘输入、按下回车键才会继续往下执行；
		//返回值bool：
		//true：成功读取到一行有效输入，进入 if 代码块取值；
		//false：输入流关闭、IO 报错、强制结束输入，读取失败，不进入 if。
		//Scan () 负责等输入、收数据、存内部缓存、告知成败，数据是它读进来的，只是暂存在内部，没交给你变量。

		nameNew := scanner.Text()
		//scanner.Text()
		//只有scanner.Scan()返回 true 后才能调用；
		//作用：从 Scanner 内部缓冲区，取出上面 Scan () 成功读取到的字符串,自动剥离结尾回车换行符；
		//优势：完整保留空格、中文、特殊符号，比如输入张三 三可以完整读取，不会截断。
		//把读取到的整行字符串赋给变量nameNew，后续打印、逻辑处理都用这个变量。
		fmt.Println("Scanner读取整行：", nameNew)
	}
	if err := scanner.Err(); err != nil {
		//作用：区分「正常手动结束输入」和「读写异常崩溃」
		//正常手动结束：scanner.Scan()返回 false，Err()返回 nil，不算错误；
		//硬件 / IO 错误：控制台输入流损坏、管道中断、权限问题，会捕获真实错误。
		//规范写法：Scanner 读取结束后，推荐必写，排查输入异常问题，养成工程健壮性习惯。
		fmt.Println("读取输入发生错误：", err)
	}

	// ==============================================================================
	// 模块2：读取数字菜单选择（对比 fmt.Scanf）
	// ==============================================================================
	fmt.Println("\n========= 模块2：读取数字菜单 =========")
	fmt.Println("1.测试A  2.测试B  0.退出")
	fmt.Print("请输入选项数字：")
	/*
		// 老式方案 注释掉 读完数字残留换行，污染下一次输入
		var selectOld int
		fmt.Scanf("%d", &selectOld)
		fmt.Println("老式Scanf选择：", selectOld)
	*/

	//Scanner 只能读取字符串文本，没法直接拿到数字；
	//所以流程是：键盘输入全部先读成字符串，再用 fmt.Sscan 把字符串转成 int，用来做菜单数字选择。
	scanner.Scan()
	//阻塞等待你输入菜单数字，按下回车；
	//把你输入的一整行文字存入 scanner 内部缓存，自动去掉末尾换行；
	//这里省略了 if 判断，默认用户会正常输入；严谨写法建议包一层 if scanner.Scan()。
	var selectNew int
	count, err := fmt.Sscan(scanner.Text(), &selectNew)
	// scanner.Text()
	//取出上一步 Scan 读到的字符串，示例是 "3"；
	//fmt.Sscan(字符串, &变量)
	//功能：解析字符串里的内容"3"，匹配类型写入对应变量selectNew；
	//两个返回值（必须都接收，不能只写一个）
	//count int：成功赋值了几个变量。这里只传了 &selectNew，转数字成功就是 1，失败就是 0；
	//err error：转换失败时不为空（比如输入字母、符号）。
	if count != 1 || err != nil {
		fmt.Println("输入不是合法数字！")
	} else {
		fmt.Println("Scanner转换数字，你选择：", selectNew)
	}

	// ==============================================================================
	// 模块3：输入带空格内容（老式函数重大缺陷演示）
	// ==============================================================================
	fmt.Println("\n========= 模块3：输入带空格昵称（关键对比） =========")
	fmt.Print("请输入带空格昵称，例如：小明同学：")
	/*
		// 老式Scanf/Scan 遇到空格直接截断，只能读到第一个词
		var nickOld string
		fmt.Scan(&nickOld)
		fmt.Println("老式Scan截断结果：", nickOld)
	*/

	// Scanner 完整读取整行，空格全部保留
	scanner.Scan()
	nickNew := scanner.Text()
	fmt.Println("Scanner完整读取（包含空格）：", nickNew)

	// ==============================================================================
	// 模块4：循环持续输入（贴合你聊天室Run菜单循环场景）
	// ==============================================================================
	fmt.Println("\n========= 模块4：循环持续输入（聊天室同款） =========")
	fmt.Println("输入内容回车打印，输入quit结束循环")
	loopScanner := bufio.NewScanner(os.Stdin)
	for loopScanner.Scan() {
		input := loopScanner.Text()
		if input == "quit" {
			fmt.Println("循环输入结束")
			break
		}
		fmt.Printf("你发送的消息：%s\n", input)
		fmt.Print("继续输入：")
	}
}

/*
Scanner 核心 API 速记（背诵版）
bufio.NewScanner(os.Stdin)
创建扫描器，绑定控制台标准输入，只创建一次 / 循环内均可新建
scanner.Scan()
阻塞等待用户输入，按下回车执行读取；
返回 true = 正常读到一行；false = 关闭输入 / IO 错误
scanner.Text()
获取本次输入完整字符串，自动删除末尾回车换行\n，解决缓存残留核心方法
scanner.Err()
循环结束后调用，判断读取过程中是否出现网络 / IO 异常
*/
