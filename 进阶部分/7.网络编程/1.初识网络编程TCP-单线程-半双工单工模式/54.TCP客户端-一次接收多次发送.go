package main

/*
=======================================================================
【TODO: 未来进阶与优化方向】（当前单步版客户端的不足与改进计划）
=======================================================================

1. 交互缺陷：无法实现“全双工”实时聊天
   - 当前不足：程序卡在 `fmt.Scanln(&text)` 等待键盘输入时，底层的 `conn.Read` 根本没在运行。此时如果服务端主动发来消息，客户端在控制台是看不见的，必须等敲完一次回车后才能去收消息。
   - 解决措施：使用 goroutine 分离读写逻辑。
     -> 主线程（main）放一个死循环，专门负责 `fmt.Scanln` 获取输入并 `Write` 发送。
     -> 单独开一个后台协程 `go func() { for { conn.Read... } }()`，专门盯住服务端，一有消息立刻在控制台打印。

2. 数据乱码隐患：缺乏序列化与封包
   - 当前不足：纯文本 `[]byte(text)` 直接发送。如果打字速度极快或网络拥堵，发出去的字符串在服务端会粘成一坨。
   - 解决措施：与服务端保持协议一致。每次发送前，把字符串封装成 `[消息长度][真实文本内容]` 的二进制包后再执行 `Write`。

3. 稳定性缺陷：断线重连机制
   - 当前不足：网络一断或者服务端挂掉重启，客户端就会报 `wsasend` 错误并直接退出进程。
   - 解决措施：捕获 `Write` 或 `Read` 的错误，在断线时触发一个 `for` 循环结合 `time.Sleep` 去尝试重新 `net.Dial`，实现商业级 App 常见的“断线自动重连”功能。
=======================================================================
*/


import (
	"fmt"
	"io"
	"net"
)

func main() {

	/*
		1. 连接 TCP 服务端：Dial 是客户端核心函数，参数1=协议类型，参数2=服务端地址+端口
	       作用：与服务端建立 TCP 连接（三次握手），成功返回 Conn 连接对象，失败返回 error
		   阻塞特性：Dial 是阻塞方法，直到连接建立成功/超时/失败才会返回
	*/
	conn, err := net.Dial("tcp", "127.0.0.1:8080")
	if err != nil {
		fmt.Println("连接失败")  // 连接失败场景：服务端未启动、地址/端口错误、网络不通等
		return
	}

	// 【重要】defer 延迟执行：确保函数退出前关闭连接，避免资源泄漏
	// 即使后续代码出错，defer 也会执行 conn.Close()，释放连接句柄
	defer func() {
		_ = conn.Close()
		fmt.Println("客户端连接已关闭")
	}()

	var byteData=make([]byte,1024)  //// 2.1 初始化字节切片：byteData用于存放读取到的数据
	/*
		  2.2 读取服务端数据：Conn.Read() 是阻塞方法，直到读到数据/连接关闭/出错
			  返回值n：实际读取到的字节数（<= 切片长度）
			  返回值err：读取错误（如连接关闭会返回 io.EOF）
	*/
	n,err:=conn.Read(byteData)  //从conn通道中，拿到数据并放到byteData中   “装进”
	if err==io.EOF{
		fmt.Println("接收数据完成")
	} else if err != nil { // 【修改】增加了 else if 防止读取失败时依旧打印
		fmt.Println("读取数据失败:", err)
	} else {
		fmt.Println("接收到的字节数:", n, " 接收到的数据:", byteData[:n])
	}


	for{
		var text string  //用于发送给服务器端的数据
		fmt.Println("请输入要发送的数据：")
		fmt.Scanln(&text)
		if text == "exit"{
			// conn.Close()  【修改】因为上面已经写了 defer 关闭，这里不用重复调用
			break
		}

		// 【修改】增加了写入错误的判断，防止服务端关闭后客户端继续发送导致崩溃
		_, err := conn.Write([]byte(text))
		if err != nil {
			fmt.Println("发送失败，连接可能已被服务端关闭:", err)
			break
		}
	}

}
