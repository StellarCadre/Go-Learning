import (
	"fmt"
	"time"
)

var wait int

func Shopping(str string) {
	fmt.Println(str, "在购物")
	time.Sleep(1 * time.Second)
	fmt.Println(str, "购物结束")

	wait--
}

func main() {
	startTime := time.Now()
	wait = 3
	//这种模式属于接力，只有等前一个函数结束之后，才能运行当前函数，容易造成时间线拉长。如以下函数总时间3s。（先来先服务）
	//Shopping("张三")
	//Shopping("李四")
	//Shopping("王五")

	//这种属于Go的协程方式，即同时运行下面的三条语句。（时间片轮转法吗？）
	/*
		但是目前的该方案存在严重的问题，主进程没有上锁，程序在执行完下面的第三条语句后，主进程提前关闭，导致整个程序终止，并没有等全部执行完毕。
	    主线程结束，协程函数被迫终止。
	*/
	go Shopping("张三") //函数前加go，表示该函数是协程函数
	go Shopping("李四")
	go Shopping("王五")

	/*
		为了解决上面的问题，可以在此处进行等待，如time.Sleep(a*time.Second)。
		但是并不知道需要等待多长时间，即a不确定。
		故，可以考虑使用一个变量wait来接收总耗时，然后进行判断,如下：
		但是当前的解决方案，可能遇到这些问题：
		多协程同时修改变量wait，数据错乱。死循环空转 → 疯狂消耗 CPU。代码脆弱，难维护、难扩展。
		综上所述，必须引入go中标准的sync.WaitGroup，见下一个代码文件。
	*/
	for {
		if wait == 0 {
			break
		}
	}
	fmt.Println("购买业务完成,总耗时：", time.Since(startTime))
}

/*
===================== 一、Goroutine 基础概念 =====================
1. 定义：Go 语言独有的轻量级协程，由 Go 运行时(runtime)管理，不是操作系统线程
2. 与 OS 线程区别：
   - OS线程：栈MB级、切换开销大、数量上限几千
   - goroutine：初始栈2KB，自动扩缩容，切换仅保存少量寄存器，单机可开百万级
3. 启动关键字：go 函数/匿名函数
4. 主协程(main goroutine)：main函数本身就是goroutine，main退出则所有子goroutine强制销毁
5. 调度模型：M/P/G 调度器（GMP）
   G(goroutine)：协程本体，存储栈、程序计数器、状态
   M(machine)：操作系统内核线程，真正执行代码
   P(processor)：调度器逻辑处理器，持有运行资源，绑定M执行G
   核心：P实现M与G解耦，不用频繁创建销毁OS线程
6. 并发模型：CSP(Communicating Sequential Processes)
   理念：不要通过共享内存通信，要通过通信共享内存（channel）
*/

/*
===================== 二、goroutine 启动方式 =====================
1. 普通函数：go funcName()
2. 匿名函数（常用，支持传参）：go func(v T){}(val)
3. 注意：直接在循环go匿名函数会捕获循环变量地址，产生数据竞争，必须传参拷贝
*/

/*
===================== 三、goroutine 同步等待（核心3种方案） =====================
1. time.Sleep：简单测试用，不适合生产，无法精准等待
2. sync.WaitGroup：批量等待一组goroutine，无返回值场景
   - Add(n)：设置等待计数器增量，必须在goroutine外调用
   - Done()：计数器-1，每个goroutine执行完调用
   - Wait()：阻塞直到计数器归0
3. channel 同步：
   - 无缓冲channel：收发阻塞，天然同步
   - 带缓冲channel：可做信号量控制并发数量
4. 补充：sync.Mutex/sync.RWMutex 解决共享变量竞争，不是等待协程
*/

/*
===================== 四、goroutine 通信 Channel =====================
1. channel 是goroutine之间唯一推荐通信方式，线程安全
2. 分类：
   无缓冲通道：make(chan T)，发送后必须等接收，接收前必须等发送，同步阻塞
   有缓冲通道：make(chan T, size)，缓冲区未满可异步发送，缓冲区空才阻塞接收
3. 操作：
   ch <- val 发送数据
   v := <-ch 接收数据
   close(ch) 关闭通道，只能发送方关闭；关闭后再发送会panic，接收可正常取零值
4. range 遍历channel：for v := range ch 会持续接收，直到通道关闭
5. select 多路监听：同时监听多个channel读写，随机执行就绪分支；搭配default非阻塞
*/

/*
===================== 五、goroutine 终止规则 =====================
1. 正常结束：函数执行完毕，goroutine自动退出
2. 主动终止：return
3. 被动销毁：main goroutine退出，所有子goroutine直接杀死，不等待执行完
4. 无法外部强制杀死goroutine：没有kill接口，只能通过channel信号主动退出
5. 泄漏风险：goroutine阻塞在channel读写、死锁，会永久占用内存，造成goroutine泄漏
*/

/*
===================== 六、并发安全与竞争 =====================
1. 数据竞争：多个goroutine同时读写同一个变量，未同步，结果错乱
2. 解决方案：
   ① channel 通信（推荐，CSP思想）
   ② sync.Mutex 互斥锁：同一时间仅一个goroutine访问临界区
   ③ sync.RWMutex 读写锁：读多写少场景，读可并发，写独占
3. 检测工具：go run -race 代码，自动检测数据竞争
*/

/*
===================== 七、常用工具包函数 =====================
1. runtime.GOMAXPROCS(n)：设置P的数量（逻辑处理器），默认等于CPU核心数，控制并行度
2. runtime.Goexit()：仅退出当前goroutine，不影响其他协程，main中调用会直接退出程序
3. runtime.NumGoroutine()：获取当前运行的goroutine总数量，调试用
4. time.After()：返回单向channel，超时后发送当前时间，搭配select做超时控制
*/

/*
===================== 八、单向通道（规范约束） =====================
chan<- int 只写通道：函数内只能发送数据，防止误读
<-chan int 只读通道：函数内只能接收数据，防止误关闭发送
作用：编译期约束通道操作，提高代码安全性
*/

/*
===================== 九、死锁场景（高频踩坑） =====================
1. 无缓冲通道，只发送不接收 / 只接收不发送
2. 多个goroutine互相等待对方通道数据，循环阻塞
3. WaitGroup Add/Done不匹配（Done多于Add、忘记Done）
4. range遍历未关闭的channel，永久阻塞
*/

/*
===================== 十、goroutine 并发控制（限流） =====================
缓冲channel信号量：创建固定长度缓冲chan，启动goroutine前写入占位，执行完释放，控制最大并发数
示例：limit := make(chan struct{}, 10) 最多同时10个协程运行
*/
