package main

func plus(a int, b int) int {
	return a + b
}


func main() {
    plus(1, 2) //1和2都是int型，可以调用

	var u1,u2=uint(1),uint(1)
	//plus(u1,u2)  //u1和u2是uint型，这样调用会报错：无法将 'u1/u2' (类型 uint) 用作类型 int。可明明都属于int家族。
	plus(int(u1), int(u2))  //未引入泛型时的解决方法：强制类型转换，或者重新定义一个uint的plus。
}



/*
【Go 泛型 学习前瞻】

1. 泛型是什么？
   一句话：让函数/结构体支持“多种类型”，但不用写重复代码。
   以前要给 int、string 各写一套，现在一套通用。

2. 为什么要用泛型？
   • 消除重复代码（比如写一个通用切片工具函数）
   • 保留类型安全（不会像 interface{} 一样丢失类型）
   • 代码更简洁、更通用

3. 泛型用在哪里？
   • 通用工具函数（切片、map、队列、栈、链表）
   • 通用数据结构（通用切片、通用树、通用链表）
   • 不想用 interface{} 丢失类型检查的场景

4. 泛型核心三要素
   • 类型参数 [T any] 或 [T TypeConstraint]
   • 类型约束 any / comparable / 自定义
   • 类型推导（不用手动指定类型，自动推断）

5. 最简单理解
   泛型 = “模板”
   T = 占位符（代表未来传入的任意类型）

6. 新手最容易懵的符号
   [T any] —— 这就是泛型！表示 T 可以是任何类型

7. 工程意义
   让 Go 拥有真正的通用工具库，
   但不丢失类型安全，不降低性能。

一句话总结：
泛型 = 一套代码，支持所有类型，且类型安全！
*/
